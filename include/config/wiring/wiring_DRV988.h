#ifndef CONFIG_WIRING_DRV988_H
#define CONFIG_WIRING_DRV988_H

// DRV8833 mapping:
// ESP32 GPIO21 -> AN2
// ESP32 GPIO20 -> AN1
// ESP32 GPIO9  -> BN2
// ESP32 GPIO8  -> BN1
// ESP32 GPIO5  -> STBY
#define PIN_DRV8833_AN2          GPIO_NUM_21
#define PIN_DRV8833_AN1          GPIO_NUM_20
#define PIN_DRV8833_BN2          GPIO_NUM_9
#define PIN_DRV8833_BN1          GPIO_NUM_8
#define PIN_DRV8833_STBY         GPIO_NUM_5

#endif // CONFIG_WIRING_DRV988_H
